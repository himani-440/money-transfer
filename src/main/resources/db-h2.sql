DROP TABLE IF EXISTS account;

CREATE TABLE account (
  ID INT NOT NULL AUTO_INCREMENT,
  NUMBER VARCHAR,
  STATUS VARCHAR,
  BALANCE DOUBLE,
  CREATED_TIME TIMESTAMP,
  MODIFIED_TIME TIMESTAMP,
  CONSTRAINT pk_t_account PRIMARY KEY (ID)
);

